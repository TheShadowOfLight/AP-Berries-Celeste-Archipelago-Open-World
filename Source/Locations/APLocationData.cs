        public static int GetLocationCount(LocationType type, int level, int mode)
        {
            int count = 0;
            string level_mode = $"{level}_{mode}";
            switch (type)
            {
                case LocationType.Checkpoint:
                {
                    foreach (string location in Celeste_MultiworldModule.SaveData.CheckpointLocations)
                    {
                        if (location.StartsWith(level_mode) && CheckpointStringToID.Keys.Contains(location))
                        {
                            count++;
                        }
                    }
                    break;
                }
                case LocationType.Key:
                {
                    foreach (string location in Celeste_MultiworldModule.SaveData.KeyLocations)
                    {
                        if (location.StartsWith(level_mode))
                        {
                            count++;
                        }
                    }
                    foreach (string location in Celeste_MultiworldModule.SaveData.GemLocations)
                    {
                        if (location.StartsWith(level_mode))
                        {
                            count++;
                        }
                    }
                    break;
                }
                case LocationType.Binoculars:
                {
                    foreach (string location in Celeste_MultiworldModule.SaveData.BinocularsLocations)
                    {
                        if (location.StartsWith(level_mode))
                        {
                            count++;
                        }
                    }
                    break;
                }
                case LocationType.Room:
                {
                    foreach (string location in Celeste_MultiworldModule.SaveData.RoomLocations)
                    {
                        if (!RoomNameToAP.ContainsKey(location))
                        {
                            continue;
                        }

                        if (location.StartsWith(level_mode))
                        {
                            count++;
                        }
                    }
                    break;
                }
            }

            return count;
        }

        public static int GetLocationTotal(LocationType type, int level, int mode)
        {
            int count = 0;
            string level_mode = $"{level}_{mode}";
            switch (type)
            {
                case LocationType.Checkpoint:
                {
                    foreach (string location in CheckpointStringToID.Keys)
                    {
                        if (level == 10)
                        {
                            string room = location.Replace(level_mode, "");
                            if (!ArchipelagoManager.Instance.ActiveLevels.Contains("10c") && room.Contains("end-golden"))
                            {
                                continue;
                            }

                            if (!ArchipelagoManager.Instance.ActiveLevels.Contains("10b") && (room.Substring(1, 1).CompareTo("f") >= 0))
                            {
                                continue;
                            }
                        }

                        if (location.StartsWith(level_mode))
                        {
                            count++;
                        }
                    }
                    break;
                }
                case LocationType.Key:
                {
                    foreach (string location in KeyIDToAP.Keys)
                    {
                        if (location.StartsWith(level_mode))
                        {
                            count++;
                        }
                    }
                    foreach (string location in GemIDToAP.Keys)
                    {
                        if (location.StartsWith(level_mode))
                        {
                            count++;
                        }
                    }
                    break;
                }
                case LocationType.Binoculars:
                {
                    foreach (string location in BinocularsIDToAP.Keys)
                    {
                        if (level == 10)
                        {
                            string room = location.Replace(level_mode, "");
                            if (!ArchipelagoManager.Instance.ActiveLevels.Contains("10c") && room.Contains("end-golden"))
                            {
                                continue;
                            }

                            if (!ArchipelagoManager.Instance.ActiveLevels.Contains("10b") && (room.Substring(1, 1).CompareTo("f") >= 0))
                            {
                                continue;
                            }
                        }

                        if (location.StartsWith(level_mode))
                        {
                            count++;
                        }
                    }
                    break;
                }
                case LocationType.Room:
                {
                    foreach (string location in RoomNameToAP.Keys)
                    {
                        if (level == 10)
                        {
                            string room = location.Replace(level_mode, "");
                            if (!ArchipelagoManager.Instance.ActiveLevels.Contains("10c") && room.Contains("end-golden"))
                            {
                                continue;
                            }

                            if (!ArchipelagoManager.Instance.ActiveLevels.Contains("10b") && !room.Contains("intro") && (room.Substring(1, 1).CompareTo("f") >= 0))
                            {
                                continue;
                            }
                        }

                        if (location.StartsWith(level_mode))
                        {
                            count++;
                        }
                    }
                    break;
                }
            }

            return count;
        }

